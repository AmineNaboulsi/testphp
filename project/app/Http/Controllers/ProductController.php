    /**
     * Remove the specified resource from storage.
     */
    public function destroy(Request $request)
    {
        $productId = $request->header('id');
        $product = Product::find($productId);

        if ($product) {
            $product->delete();
            return json_encode([
                "status" => "Deleted Successfully"
            ]);
        } else {
            return json_encode([
                "status" => "Product not found"
            ]);
